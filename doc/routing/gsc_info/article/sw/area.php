		
<p id="heading"> Parkeringsplatser och parkeringshus - materialet</p>
				
<P>
<br>
					
Parkeringsplatser i Åbo är en öppen data kollektion. Den konsisterar om parkeringsplatser och parkeringshus som har åtminstone en ingång. Denna kollektion innhåller i allmänhet inte gatuparkering eller realtidsinformation om lediga platser.
