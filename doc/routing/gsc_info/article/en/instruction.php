			<p id="heading">Parking areas data set</p><br>
			<p>Parking areas dataset is available as database dump along with metadata, instructions and examples of usage (SQL queries).</p>
			<br><p>Parking area contains park_lots that consists of one or more segments that each have at least one driving entrance.
			 By default exit and entrance have the same location. Each entrance and exit have latitude and longitude.</p><br>
			<p>The address of a parking area is the address of its main (first) segment's main (first) entrance.</p>
 
