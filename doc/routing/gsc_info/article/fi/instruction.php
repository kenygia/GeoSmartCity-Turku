			<p class="heading1">Pysäköintialueet</p>
			<p>Pysäköintialueet materiaali tietoaineisto on tarjolla SQL Server tietokantascriptinä ja MySQL dump tiedostona. Molemmista muodoista
			 on ladattavissa myös metadata, käyttöohjeet ja käyttöesimerkkejä.</p>
			<br><p>Pysäköintialueet sisältävät alueita (park_lots) jotka koostuvat vähintään yhdestä segmentistä, jolla on vähintään yksi sisäänajo. 
			Oletuksena sisäänjo ja ulosajo ovat samat. 
		Jokaisella sisäänajolla on osoite ja koordinaatit (latitude ja longitude).</p><br>
			<p>Alueen osoite on ensimmäisen segmentin ensimmäisen sisäänajon osoite (main entrance).</p>
 
